string = "Hello World!"
print("Length of the string:" , len(string))
print("uppercase:",string.upper())
print("lowercase:",string.lower())
print("capitalize:",string.capitalize())

print("replace:",string.replace("Hello","Hi"))
print("split:",string.split(" "))

print("find:",string.find("World"))

print("count:",string.count("l"))

print("isalpha:",string.isalpha())
print("isdigit:",string.isdigit())

print("isalnum:",string.isalnum())
print("isspace:",string.isspace())

print("islower:",string.islower())

print("isupper:",string.isupper())

print("istitle:",string.istitle())

print("startswith:",string.startswith("Hello"))
print("endswith:",string.endswith("World!"))

print("join:",string.join("Hello"))

print("ljust:",string.ljust(20))
print("rjust:",string.rjust(20))
print("center:",string.center(20))

print("lstrip:",string.lstrip())
print("rstrip:",string.rstrip())

print("strip:",string.strip())

print("swapcase:",string.swapcase())

print("title:",string.title())

print("expandtabs:",string.expandtabs())

print("zfill:",string.zfill(20))

print("partition:",string.partition("World"))
print("rpartition:",string.rpartition("World"))

print("splitlines:",string.splitlines())

print("format:",string.format("Hello"))

print("format:",string.format("Hello","World"))

print("format:",string.format("Hello","World","!"))

print("format:",string.format("Hello","World","!","How"))

print("format:",string.format("Hello","World","!","How","are"))                   

